-- Comparação entre conjuntos
-- ALL -> o dado tem que ser maior ou menor ou igual que todos os dados do conjunto
-- ANY -> o dado tem que ser maior ou menor ou igual que qualquer dado do conjunto
-- SOME -> executa ambos, tem que ser maior ou menor ou igual do que alguns dados do conjunto

-- UNION -> retorna todas as linhas, união com o conjunto
-- INTERSECT -> retorna as linhas compartilhadas com o conjunto
-- MINUS/EXCEPT -> retorna as linhas exclusivas da consulta e do conjunto